        // TODO add your handling code here:
        
         DbUsuarios objx = new DbUsuarios();
         DbUsuariosConsulta objy = new DbUsuariosConsulta();
         
         String pass = new String(PwdLog.getPassword());
         
         if(!UsuLog.getText().equals("") && !pass.equals("") ){
             
             String NuevoPass = hash.sha1(pass);
            objx.setUsuario(UsuLog.getText());
            objx.setPassword(NuevoPass);
            
            if(objy.Login(objx)){
                
        
           Inicio obj = new Inicio();
           obj.setVisible(true);
           dispose();
                
                
            }else{
                JOptionPane.showMessageDialog(null, "los datos son incorrectos");
            }
             
         }else{
            JOptionPane.showMessageDialog(null, "los datos estan vacios");
        }
 
          
